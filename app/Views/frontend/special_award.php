            <p class="why-choose-two__text">
            The Foundation presents special awards to administrators/public figures to recognize their contribution to the society, for dealing effectively with various serious public health problems being faced by the people. The Foundation honoured the following persons with such special awards
            </p>
            <p class="why-choose-two__text">
                    The first award in this category was presented to Mr. S.R. Rao, Municipal Commissioner of Surat for cleaning the city, following the plague epidemic. This was organized at India International Centre, New Delhi on March 13, 1997. Mr. Rao delivered the public address entitled "Cleaning up our cities – The Surat experience".
            </p>
            <p class="why-choose-two__text">
                    The second award in the 
                    field of Public health was presented to Dr. Justice K. Narayana Kurup, Former Acting Chief Justice and Judge, High Court of Madras, in recognition of his outstanding contributions on wide ranging issues of societal concern and especially his relentless crusade against tobacco. The award was presented at a special function organized at India Habitat Centre, New Delhi on March 31, 2003. Justice Kurup delivered a public address entitled "Of Benevolent Verdicts" after receiving the award
            </p>
            <p class="why-choose-two__text">
                    The Foundation presented its 3rd Award in the field of Public Health to Prof. K. Srinath Reddy, President, Public Health Foundation of India and Professor of Cardiology, All India Institute of Medical Sciences, in recognition of his outstanding contributions to health promotion and prevention of cardiovascular and other chronic diseases. Prof. Reddy through his research helped to identify the risk factors contributing to the rapid rise of heart diseases in India. He has also created several new initiatives of health promotion and disease prevention like HRIDAY-SHAN. Prof. Reddy has also been globally acclaimed for his role in championing tobacco control. The award was presented at a special function organized at India Habitat Centre, New Delhi on December 29, 2006.
            </p>

            <p class="why-choose-two__text">        
                    Dr. M R Rajagopal, MD, MNAMS          

                    Chairman, Pallium India (Trust)

                    Director, Trivandrum Institute of Palliative Sciences

                    Trivandrum, Kerala, India 
            </p>
            <p class="why-choose-two__text">

                    M R Rajagopal is a pioneer in Palliative Care in India. While serving as Professor of Anesthesiology in Government Medical College at Calicut, Kerala, Dr Rajagopal explored the fields of pain and its medical management in the clinical setting. For those who faced life-threatening or terminal illnesses, pain was not the only cause of suffering. Palliative Care addresses all elements of suffering and their remedial measures. The need and opportunity for organized services in Calicut motivated Dr Rajagopal to take on the challenge of a new direction in his profession, namely Palliative Care.
            </p>
            <p class="why-choose-two__text">
                    After obtaining training at Oxford in Palliative Medicine in 1993, Dr Rajagopal established a Palliative Care Service in the Medical College, with support from his colleagues. For expanding service to the community, he and his colleagues established a Pain and Palliative Care Society, a non-governmental organization. Through its volunteer workers – doctors, nurses, and lay helpers – the Society reached out to those in need in their homes. The volunteers were trained in symptomatic pain relief and psycho-social support and counseling. Today there are over 100 trained palliative care providers in Kerala State. Recognition of this innovative and pioneer programme came from the World Health Organization in 1996. Working closely with the WHO Collaborating Centre on Pain Policy in Madison, Wisconsin, USA, the Society under Dr Rajagopal’s leadership succeeded in the Government of India evolving a policy to simplify access to morphine for the legitimate use for pain relief.
            </p>
            <p class="why-choose-two__text">
                    Dr Rajagopal conducted workshops on palliative care in 18 States in India, 14 of which have already simplified the rules on narcotics for palliative care. The old rules denied morphine even to the terminally ill cancer patients and were recognized to be inhuman. Recently he has been instrumental in establishing two palliative care training centres in Thiruvananthapuram and Hyderabad, the latter in the Regional Cancer Center. Till 2003 much of the progress in professional palliative care was confined to Kerala; therefore Dr Rajagopal created an organization called Pallium India (www.palliumindia.org) to propagate the message of the need for palliative medicine in the rest of the country. Pallium India has catalyzed the establishment of palliative care services in 8 North and North-East States.
            </p>
            <p class="why-choose-two__text">
                    Dr Rajagopal has recently prepared National Standards in Palliative Care. Moreover, Dr Rajagopal has adapted for India’s context, the International Curriculum for Education in Palliative and End-of-life Care. In 2006 Dr Rajagopal initiated the country’s first University-approved Diploma Course in Palliative Medicine. Influenced by advocacy by Dr. Rajagopal and colleagues, the Medical Council of India has accepted this branch of sub-specialty and approved an MD course in Palliative Medicine.
            </p>
            <p class="why-choose-two__text">
                    In recognition of the pioneering and visionary services in caring for the terminally or chronically ill with pain and suffering, providing service through voluntarism to thousands in need, establishing training programmes and influencing national and State policies on palliative care, this Award is given to Dr. M.R. Rajagopal, Chairman of Pallium India and Director of the Trivandrum Institute of Palliative Sciences.
            </p> 

