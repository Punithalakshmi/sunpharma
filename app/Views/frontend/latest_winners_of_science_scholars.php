                    <p>Mr. Deepak Kumar Sahel has been selected for the Sun Pharma Science Foundation Science Scholar Award - 2022 for Young Scientists in the field of Pharmaceutical Sciences. His award-winning work entitled “Design and development of non-viral lipopolymeric nanocarrier for the delivery of CRISPR/Cas9 ribonucleoprotein for the treatment of retinal dystrophic conditions”. His research was focused on the synthesis and characterization of cationic amphiphilic lipopolymer, followed by the development of CRISPR/Cas9 RNPs containing nanoformulation. The nanoformulation showed particle size of <200 nm with >90% complexation efficiency and exhibited good transfection efficiency, gene editing (%) efficiency, and in vivo stability. Further, to improve the in vivo efficiency, cRGD-targeted lipopolymeric formulations were developed, which showed good vitreous penetrability and were found to transfect retinal cells in vivo after 48h of intravitreal injection in rats. Additionally, as per the toxicity studies, the lipopolymeric nanocarrier was found safe and didn’t cause any visible toxicity at the therapeutic dose. Collectively, his research provided a detailed exploration of the role of non-viral lipopolymeric nanocarrier in delivering CRISPR/Cas9 components to the back of the eye to treat retinal diseases. <br></p>
