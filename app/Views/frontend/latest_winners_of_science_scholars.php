                    <p>Ms. Rachana Rao Battaje has been selected for The Sun Pharma Science Foundation Science Scholar Award 2022 for young scientists in the field of Pharmaceutical Sciences. Her award-winning work was titled “Exploring the bacterial cell division protein, FtsZ from the respiratory pathogen, Streptococcus pneumoniae as a target for anti-pneumococcal therapy.” Her work utilized the intrinsic fluorescence properties of Streptococcus pneumoniae FtsZ to discover a conformational switch during its polymerization, a phenomenon important for initiation of cell division in bacteria. Her work also led to identification of three FtsZ-targeting agents that show anti-proliferative activity against pathogenic Streptococcus pneumoniae, including the biomolecule, Vitamin K3. Rising anti-microbial resistance calls for the urgent development of new anti-bacterial agents and her work presents targeting FtsZ and inhibition of cell division as an attractive alternative strategy for anti-pneumococcal therapy. <br></p>
